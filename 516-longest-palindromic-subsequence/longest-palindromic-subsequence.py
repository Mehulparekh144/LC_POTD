class Solution:
    def longestPalindromeSubseq(self, s: str) -> int:
        s1 = s
        s2 = s[::-1]

        n = len(s1)
        dp = [[0 for _ in range(n+1)] for _ in range(n+1)]

        for i in range(n-1 , -1 , -1):
            for j in range(n-1 , -1 , -1):
                if s1[i] == s2[j]:
                    dp[i][j] = 1 + dp[i+1][j+1]
                else:
                    dp[i][j] = max(dp[i+1][j] , dp[i][j+1])
        
        return dp[0][0]
